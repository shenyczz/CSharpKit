namespace CSharpKit.GeoApi.Geometries
{
    /// <summary>
    /// Precision Models - 精度模型
    /// </summary>
    public enum PrecisionModels
    {
        /// <summary>
        /// double-precision floating-point based on the IEEE-754 standard
        /// </summary>
        Floating = 0,
        /// <summary>
        /// single-precision floating-point based on the IEEE-754 standard
        /// </summary>
        FloatingSingle = 1,
        /// <summary>
        /// Fixed Precision - 固定精度表示坐标有固定的小数位数。
        /// </summary>
        Fixed = 2
    }















}
